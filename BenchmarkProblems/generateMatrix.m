%% make_mat_from_benchmarks.m
% Auto-generate .mat files for all benchmark graphs listed in `names`.

clear; clc;

% --- Paths to original benchmark .m scripts ---
addpath algorithms
% strongly regular graphs
addpath('benchmark/LAT', 'benchmark/LSN', 'benchmark/LSP', ...
        'benchmark/PAN', 'benchmark/PAP', 'benchmark/STH', 'benchmark/TRI');
% component-based graphs
addpath('benchmark/CHH', 'benchmark/TNN', 'benchmark/USR');
% other graph families
addpath('benchmark/R1N');

% --- List of graph names (from your code) ---
names = { ...
    'Lattice_4_16', ...
    'Lattice_6_36', ...
    'latin_2_4', ...
    'latin_4_16', ...
    'latin_6_36', ...
    'latin_3_9', ...
    'latin_5_25', ...
    'latin_7_49', ...
    'paley_power_9', ...
    'paley_power_25', ...
    'paley_prime_13', ...
    'paley_prime_29', ...
    'sts_19_57', ...
    'Triangular_7_21', ...
    'Triangular_10_45', ...
    'CHH_cc_1_1_22_1', ...
    'CHH_cc_2_1_44_1', ...
    'tnn_1_26_1', ...
    'tnn_2_52_1', ...
    'usr_1_29_1', ...
    'iso_r01N_s20', ...
    'iso_r01N_s40', ...
};

% --- Output folder for .mat files ---
outdir = fullfile(pwd, "export_mat");
if ~exist(outdir, "dir")
    mkdir(outdir);
end

fprintf("Generating %d .mat files into %s\n", numel(names), outdir);

for k = 1:numel(names)
    name = names{k};
    fprintf("  - %s ... ", name);

    clear A n
    eval(name);   % run the original .m file to define A

    if ~exist("A", "var")
        error("Graph %s did not define A", name);
    end
    if size(A,1) ~= size(A,2)
        error("Graph %s is not square", name);
    end
    n = size(A,1);

    fout = fullfile(outdir, [name '.mat']);
    save(fout, 'A', 'n');

    fprintf("done.\n");
end

fprintf("All .mat files generated in %s\n", outdir);
